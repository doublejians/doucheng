package com.yaya.douban.tongcheng.types;

public class AlbumSize {
  private int[] large;
  private int[] image;
  private int[] cover;
  private int[] thumb;
  private int[] icon;

  public int[] getLarge() {
    return large;
  }

  public void setLarge(int[] large) {
    this.large = large;
  }

  public int[] getImage() {
    return image;
  }

  public void setImage(int[] image) {
    this.image = image;
  }

  public int[] getCover() {
    return cover;
  }

  public void setCover(int[] cover) {
    this.cover = cover;
  }

  public int[] getThumb() {
    return thumb;
  }

  public void setThumb(int[] thumb) {
    this.thumb = thumb;
  }

  public int[] getIcon() {
    return icon;
  }

  public void setIcon(int[] icon) {
    this.icon = icon;
  }

}
